#include  "api/api_tick.h"


#include  "api/api_log.h"
/******************************************************************************************************
** Defined
*******************************************************************************************************/

/******************************************************************************************************
**	static Parameters
*******************************************************************************************************/
uint32_t sys_freq = HAL_SYS_FREQ;

/******************************************************************************************************
**	public Parameters
*******************************************************************************************************/

/*****************************************************************************************************
**	static Function
******************************************************************************************************/

/*****************************************************************************************************
**  Function
******************************************************************************************************/

/*******************************************************************
** Parameters:		160M:50ns+ (n*20ns),delay(1)最小100ns
                    96M:50ns+ (n*33ns),delay(1)最小150ns	
			        24M:200ns+ (n*125ns),delay(1)最小600ns
** Returns:
** Description:	    ns延时函数,注意延时不准确,基本够用	
*******************************************************************/
void hal_delay_ns(uint32_t ns)
{
    if(sys_freq == 24000000L){
        if(ns <= 200) return;
        delay((ns-200) / 125);                     //24M
    }else{
        delay((ns-50) / 20);                        //160M
    }
}

/*******************************************************************
** Parameters:		    160M: 20ns
                        96M: 33ns
** Returns:			    24M: 125ns
** Description:		
*******************************************************************/
void hal_delay_us(uint32_t us)
{
    if(sys_freq == 24000000L){
        delay(us*(1000/125));           //24M
    }else{
        delay(us*(1000/20));            //160M
    }
}
void hal_delay_ms(uint32_t ms)
{
	delay(1000*ms);
}
void hal_tick_init(void)
{	
	sys_freq = hal_get_sysclk(SYSCLK);
    logd_r("sys_clk=%d\n",sys_freq);
}






